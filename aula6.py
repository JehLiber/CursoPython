#conversao de tipos, coerção
#type convertion, typecasting, coercion
# tipos imutáveis e primitivos:
# int, float, str, bool
print(int('1'), type(int('1'))) #quando voce coloca a classe no começo a "string" dentro será de finalidade -> 1 <class 'int'>
print(int('1') + 1)  # retorna : 2
print(float('1'))  # retorna : 1.0
print(float('1') + 1 ) # retorna : 2.0
print(str(1) + '1') # retorna : 11
print(bool(' ')) # retorna : True
print(bool('')) # retorna : False
print(bool(0)) # retorna : False
print(bool(1)) # retorna : True
