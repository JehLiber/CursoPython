# if  /  elif    / else
# se /se não se / se não

entrada = input ('Você quer "entrar" ou "sair"?')

# quando a condição for VERDADEIRA ele supre e pula as outras condições FALSAS
if entrada == 'entrar':
    print('Você entrou no sistema')
elif entrada == 'sair':
    print('Você saiu do sistema')
else:
    print('|Você não digitou nem entrar e nem sair.')


    